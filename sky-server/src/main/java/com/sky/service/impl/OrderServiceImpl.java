    /**
     * 用户端订单分页查询
     *
     * @param pageNum
     * @param pageSize
     * @param status
     * @return
     */
    public PageResult pageQuery4User(int pageNum, int pageSize, Integer status) {
        // 设置分页
//        PageHelper.startPage(pageNum, pageSize);

        OrdersPageQueryDTO ordersPageQueryDTO = new OrdersPageQueryDTO();
        ordersPageQueryDTO.setUserId(BaseContext.getCurrentId());
        ordersPageQueryDTO.setStatus(status);

        System.out.println(pageNum + " " + pageSize);

        ordersPageQueryDTO.setPage((ordersPageQueryDTO.getPage()-1)*ordersPageQueryDTO.getPageSize());
        ordersPageQueryDTO.setPageSize(pageSize);

        // 分页条件查询
        List<Orders> page = orderMapper.pageQuery(ordersPageQueryDTO);

        List<OrderVO> list = new ArrayList();

        Long numCount = orderMapper.countPage(ordersPageQueryDTO);

        // 查询出订单明细，并封装入OrderVO进行响应
        if (page != null && numCount > 0L) {
            for (Orders orders : page) {
                Long orderId = orders.getId();// 订单id

                // 查询订单明细
                LambdaQueryWrapper<OrderDetail> lambdaQueryWrapper = new LambdaQueryWrapper<>();
                lambdaQueryWrapper.eq(OrderDetail::getOrderId, orderId);
                List<OrderDetail> orderDetails = orderDetailMapper.selectList(lambdaQueryWrapper);

                OrderVO orderVO = new OrderVO();
                BeanUtils.copyProperties(orders, orderVO);
                orderVO.setOrderDetailList(orderDetails);

                list.add(orderVO);
            }
        }
        return new PageResult(numCount, list);
    }

